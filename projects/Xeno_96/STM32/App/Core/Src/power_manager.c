void PowerManager_EnterSuspendHandler(void)
{
    uint8_t buff[] = {INTERNAL_USB_SUSPEND_OP};
    SpiProtocol_SendBuffer(buff, sizeof(buff));
}

void PowerManager_ResumeHandler(void)
{
    uint8_t buff[] = {INTERNAL_USB_RESUME_OP};
    SpiProtocol_SendBuffer(buff, sizeof(buff));
}
